package lecture03_datatypes;

public class BooleanExample {
    public static void main(String[] args) {
        System.out.println(true);
        System.out.println(false);


        boolean firstBoolean = true;
        boolean secondBoolean = false;

        boolean isFirstNumberGreaterThanSecond = 10 > 20;
        boolean isSecondNumberGreaterThanFirst = 10 < 20;

        System.out.println(firstBoolean);
        System.out.println(secondBoolean);
        System.out.println(isFirstNumberGreaterThanSecond);
        System.out.println(isSecondNumberGreaterThanFirst);
    }
}
