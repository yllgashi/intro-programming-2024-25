package lecture05_conventions;

public class Conventions {
    public static void main(String[] args) {
        // PascalCase => First letter should be upper-case and everytime a word is ended it should start with upper-case
        // ex: HelloWorldClass, TestClass

        // camelCase => First letter should be lower-case and everytime a word is ended it should start with upper-case
        // ex: methodOne, variableTest, helloThereTest

        // UPPER_CASE => All letters should be upper-case
        // ex: PI, MAX_VALUE, MIN_VALUE
    }
}
