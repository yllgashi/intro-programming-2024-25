package lecture17_operators;

public class CompundOperators {
    public static void main(String[] args) {
        int numOne = 10;

        // numOne = numOne + 2; ..... same as
        numOne += 2; // sum-compound operator

        // numOne = numOne - 2; ..... same as
        numOne -= 5;

        // numOne = numOne * 2; ..... same as
        numOne *= 2;

        // numOne = numOne / 2; ..... same as
        numOne /= 3;

        // numOne = numOne % 2; ..... same as
        numOne %= 2;
    }
}
