package lecture17_operators;

public class IncrementDecrementOperators {
    public static void main(String[] args) {

        // pre-increment
        int numOnePre = 10;
        System.out.println(++numOnePre); // 11
        System.out.println(numOnePre); // 11

        // post-increment
        int numOnePost = 10;
        System.out.println(numOnePost++); // 10
        System.out.println(numOnePost); // 11








        // pre-increment
        int numTwoPre = 10;
        System.out.println(--numTwoPre);
        System.out.println(numTwoPre);

        // post-increment
        int numTwoPost = 10;
        System.out.println(numTwoPost--);
        System.out.println(numTwoPost);

    }
}
